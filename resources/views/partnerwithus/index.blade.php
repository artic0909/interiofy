            if (firm_start_date == '') {
                Swal.fire({
                    icon: "error",
                    title: "Oops...",
                    text: "Please enter your Start Date!",
                    footer: ''
                });
                return false;
            }
            if (city == '') {
                Swal.fire({
                    icon: "error",
                    title: "Oops...",
                    text: "Please select your city!",
                    footer: ''
                });
                return false;
            }
            if (email == '') {
                Swal.fire({
                    icon: "error",
                    title: "Oops...",
                    text: "Please enter your email!",
                    footer: ''
                });
                return false;
            }
            if (mobile_no == '') {
                Swal.fire({
                    icon: "error",
                    title: "Oops...",
                    text: "Please enter your mobile no!",
                    footer: ''
                });
                return false;
            }

            if(firm_type == undefined){
                Swal.fire({
                    icon: "error",
                    title: "Oops...",
                    text: "Please choose your Firm Type!",
                    footer: ''
                });
                return false;
            }

            if(major_category == undefined){
